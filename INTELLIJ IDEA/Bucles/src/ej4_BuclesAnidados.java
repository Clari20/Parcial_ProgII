public class ej4_BuclesAnidados {
        public static void main(String[] args) {
            for (int i = 'Z'; i >= 'A'; i--) {
                for (int j = i; j >= 'A'; j--) {
                    System.out.print((char) j);
                }
                System.out.println();
            }
        }
    }


